let a = 10;
console.log(a > 5); //mayor
console.log(a >= 5); //mayor o igual
console.log(a < 5); //menor
console.log(a <= 5); //menor o igual

console.log(a == 10); //igualdad
console.log(a != 10); //diferencia
console.log(a === '10'); //triple igualdad (mismo valor y tipo de dato)
console.log(a !== '10');